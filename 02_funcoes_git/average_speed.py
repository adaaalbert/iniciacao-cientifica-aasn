"""
This script defines a function average_speed and returns the result of division
of distance by time.
"""


def avg_speed(distance, time):
    """
    Returns the average speed by dividing distance by time.

    Parameters
    ----------
    distance : float
        Description:

    time : float
        Desc:

    Returns
    -------
    float
        Desc:

    """

    return distance / time
